//function4 (anonymous)
var add=function(a,b){
    return a+b
}
console.log(add(2,3))
//function4 (arrow)
var add=(a,b)=>{
    return a+b
}
console.log(add(2,3))

//function5 (anonymous)
var subtract=function(a,b){
    return a-b
}
console.log(subtract(3,3))
//function5 (arrow)
var subtract=(a,b)=>{
    return a-b
}
console.log(subtract(3,3))

